
// Strict Equality
console.log(1 === 1); // strict typing checks that the value and type are the same
// number 1 === number 1 returns true
// number 1 === string '1' returns false

// Loose Equality
console.log(1 == 1); // returns true
console.log(1 == '1'); // returns true
console.log(true == 1);  // returns true