let x = 10;
let y = 3;

console.log(x + y); // + addition operator
console.log(x - y); // - subtraction operator
console.log(x * y); // * multiplication operator
console.log(x / y); // / division operator
console.log(x % y); // % (modulus)remainder of division operator
console.log(x ** y); // ** exponentiation operator (x to the power of y or x^y)

// Increment Operator ++
console.log(++x); // the value of x will increase by one and then we will see the value of x
console.log(y++); // the value of y will be displayed and then increased by 1

// Decrement Operator --
console.log(x--);
console.log(--x); // Same as above but decreaseing by one