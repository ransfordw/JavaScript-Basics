
let x = 1;

// Comparison Operators
console.log(x > 0); // writes true to the console
console.log(x >= 1); // writes true to the console
console.log(x < 1); // writes false to the console
console.log(x <= 01); // writes true to the console

// Equality Operators
console.log(x === 1); // checks if x is equal to 1
console.log(x !== 1); // checks if x is not equal to 1
